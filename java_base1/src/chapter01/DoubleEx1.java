package chapter01;

public class DoubleEx1 {
	public static void main(String[] args) {
		//[Number : bit -> byte -> short -> int -> long] 일반자료형
		// 문자 : char('') -> String ("")
		// 소수:float -> double
		
		float fnum=3.14f;//4byte float 뒤에 소수에 f입력
		double dnum=3.14;//8bite
		
		System.out.println(fnum);
		System.out.println(dnum);
		
}
}